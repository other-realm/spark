  <body>
    <p>
      According to the latest census data, 18% of American adults have trouble walking or going up steps for some reason or another<a href="https://www.otherrealm.org/#_edn1" name="_ednref1" title="" id="_ednref1"><font face="Times New Roman,serif">[i]</font></a>.&#160;&#160;The inability to walk confidently without pain, discomfort, or fear of falling puts a toll, not just on the challenge of getting where you need to go, but also on having the desire to try and get there in the first place.&#160;&#160;Those who have gait abnormalities often have limited financial means to spend on custom orthotics, or do not have the wherewithal to seek it out unless a doctor or therapist suggests it.&#160;&#160;Wearing proper footwear, however, is an important preventative factor in predicting whether further injuries will occur and the amount of exercise and social engagement that will be carried out.&#160;&#160;The proper footwear therefore, can mean the difference between an individual retaining their job, leading a productive life, and contributing to society vs getting fired or going on disability insurance.&#160;&#160;Those who cannot find proper footwear often need expensive therapy and contribute to the trillions of dollars<a href="https://www.otherrealm.org/#_edn2" name="_ednref2" title="" id="_ednref2"><font face="Times New Roman,serif">[ii]</font></a>&#160;United States taxpayers currently have to spend insuring elderly and disabled adults.&#160;&#160;Low cost preventative measures are crucial to mitigating these drains on our collective pocketbook and psyche.&#160;
    </p>
    <p>
      &#160;
    </p>
    <p>
      Until recently, custom shoes were very expensive and not widely available because each one needed to be manually fabricated.&#160;&#160;However, in the last decade or so, computer controlled 3D printing has made possible the prospect of automating custom designs and the forecast for 3D-printed footwear puts the revenue potential at around $5.9 billion by the end of the decade<a href="https://www.otherrealm.org/#_edn3" name="_ednref3" title="" id="_ednref3"><font face="Times New Roman,serif"><sup>[iii]</sup></font></a>.&#160;&#160;While this technology is beginning to be rolled out for the high end sports market, it is still a nascent industry and people who diagnose and treat gait abnormalities often have limited skills in industrial design.&#160;&#160;Therefore, a need for simple tools that do not require an extensive background in many fields is a must.&#160;&#160;One aim of this study, therefore, is the automation of orthotic shoe design.&#160;&#160;In talking with existing orthotic fabricators, the current challenge in custom footwear is not directly in the technology behind 3D printing, but in the time it takes to design and print things.&#160;&#160;On the small scale, it is much more economical to create one-off shoes by hand, than it is to create them in CAD from scratch and wait to have them printed, and there is a shortage of CAD experts<a href="https://www.otherrealm.org/#_edn4" name="_ednref4" title="" id="_ednref4"><font face="Times New Roman,serif">[iv]</font></a>.&#160; However, with the advent of generative design practices, combined with open source gait analysis software using lower cost video tracking and software like <a href="https://simtk.org/projects/opensim">OpenSim</a>, the tools are available to automate the labor intensive parts of custom adaptive technology and thus lower costs.
    </p>
